package JUN_ALGORISM;

import java.util.Scanner;

public class _08_NotDoneYet {

	public static void main(String[] args) {
		
		Scanner scan = new Scanner (System.in);
		
		int N = scan.nextInt();
	
		
		if (N % 5 == 0 || N % 3 == 0) {
			
			System.out.println();
			
		}	else {
			
			System.out.println(-1);
			
		}
			
			
	}
	
}


